function charsToString(firstCharInput, secondCharInput, thirdCharInput) {
    const firstChar = firstCharInput;
    const secondChar = secondCharInput;
    const thirdChar = thirdCharInput;

    const result = firstChar + secondChar + thirdChar;
    console.log(result);
}

charsToString('a', 'b', 'c');
charsToString('%', '2', 'o');
charsToString('1', '5', 'p');